package ex1;

/**
 * The Calcul class performs simple arithmetic operations.
 */
public class Calcul {

    /**
     * Performs the addition operation between two numbers.
     *
     * @param a The first number to be added.
     * @param b The second number to be added.
     * @return The sum of the two numbers.
     */
    public int addition(int a, int b) {
        return a + b;
    }

    /**
     * Performs the subtraction operation between two numbers.
     *
     * @param a The number from which the second number will be subtracted.
     * @param b The number to be subtracted.
     * @return The difference between the first and the second number.
     */
    public int subtraction(int a, int b) {
        return a - b;
    }
}